package operator;

public class Plus2 {
	public static void main(String[] args) {
		double var1=123456789;
		double var2=1234567890;
		
		System.out.println(var1+var2);
		System.out.println(var1-var2);
		System.out.println(var1*var2);
		System.out.println(var2/var1);
		System.out.println(var2%var1);
		System.out.println(var1>var2);
		System.out.println(var1<var2);
		System.out.println(var1==var2);
		System.out.println(var1!=var2);
	}

}

