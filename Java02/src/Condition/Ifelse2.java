package Condition;

public class Ifelse2 {
	public static void main(String[] args) {
		int num=99;
		
		if(num < 10){System.out.println("10보다 작음");}
		else if(num < 100) {System.out.println("100보다 작음");}
		else if(num <1000) {System.out.println("1000보다 작음");}
		else {System.out.println("그 밖에...");}
	}

}
